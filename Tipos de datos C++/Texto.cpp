// Un dato de texto puede almacenar una secuencia de caracteres, como un nombre.
// Por ejemplo, podemos almacenar el nombre de un perro.

#include <iostream>
#include <string>  // Necesario para usar strings
using namespace std;

int main() {
    string nombrePerro = "Firulais";
    string razaPerro = "Labrador";

    cout << "Nombre del perro: " << nombrePerro << endl;
    cout << "Raza del perro: " << razaPerro << endl;

    return 0;
}

