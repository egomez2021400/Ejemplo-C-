// Un dato de punto flotante puede almacenar números con decimales.
// Por ejemplo, podemos almacenar el peso de un perro en kilogramos.

#include <iostream>
using namespace std;

int main() {
    float peso = 12.5f;  
    double altura = 0.75; 

    cout << "Peso del perro: " << peso << " kg" << endl;
    cout << "Altura del perro: " << altura << " metros" << endl;

    return 0;
}

